    // Initialize the typing test
    function init()
    {
        //load user data
        lSInit();
        //choose which text to type
        chooseText();
        //Load the typing text
        showWords(words); 
        //Listen for typing
        input.addEventListener('input', spacePressed);
        ownText.addEventListener('input', saveOwnText);
        minutes.addEventListener('input', setTime);
        //tick tock
        typingTime = setInterval(clock, 1000); 
    }
    
    //unpause button function - displays the input box again once the button is pressed, and resets the displays
    
    function unpause(){
        input.setAttribute('style', 'display: in-line;');
        wpmDisplay.innerHTML = wpm;
        errors.innerHTML = errorCount; 
        message.innerHTML = "<span class = 'text-white'>When you are ready, begin typing. <span><br>";
        input.value = ''; 
        input.placeholder = 'Start Typing...';
        showWords(words); 
    }
    
    //this function takes the user inputted number of minutes and sets the time to the number of seconds in said number of minutes 
    function setTime(){
        totalTime = 60 * Number(minutes.value);
    }
    
    //this function saves the user inputted text and loads it into the typing test. 
    function saveOwnText(){
        cText = ownText.value; //current text in ownText textbox
        pText = []; //parsed text
        sWord = ""; //current word to save
        j = 0;
        while(j < cText.length){
            while(cText[j] !== " " && j < cText.length){
                sWord = sWord + cText[j]; 
                j = j + 1; 
            }
            pText.push(sWord);
            sWord = "";
            j = j + 1;
        }
        words = pText;
        showWords(words);
    }
    
    
    // choose at random text that displays on screen
    function chooseText()
    {
        let choice = choices.length; 
        choice = (Math.floor((Math.random() * 100) + 1) % choice);
        words = choices[choice];
    }
    
    //Check if the current word in question matches the word typed 
    function matchWords()
    {
        if(!isPaused)
        {
            if(input.value === words[count] + ' ' || input.value === ' ' + words[count] + ' ')
            {
                message.innerHTML = correctText;
                return true; 
                
            } 
            else 
            {
                message.innerHTML =  incorrectText;
                errorLogger();
    
                return false;
            }
        }
    }
    
    //Show the next 8 words in the typing sequence
    function showWords(words)
    {
        currentWords.innerHTML = "<span style = 'border-radius: 5px; padding: 1px 3px 1px 3px' id = 'highlightColor'> <strong>" + words[count] +"</span>" +' ' + words[count + 1] + ' ' + words[count + 2] + ' ' + words[count + 3] + ' ' + words[count + 4]+ ' ' + words[count + 5] + ' ' + words[count + 6] +  ' ' + words[count + 7] + '</strong>';
        // + ' ' + words[count + 1] + ' ' + words[count + 2] + ' ' + words[count + 3] + ' ' + words[count + 4]+ ' ' + words[count + 5] + ' ' + words[count + 6] +  ' ' + words[count + 7] 
        style();
    }
    
    
    function clock()
    {
        
        if((time < totalTime) && isPlaying)
        {
            // Increase time
            time++;
        }
        else if (wpm > 0)
        {
            // Game is over 
            isPlaying = false;
            //Hide Input to prevent more typing until user is ready 
            input.setAttribute('style','display:none;'); 
    
            //create button to allow the user to re-enable input when ready
            message.innerHTML = "<span>Press the button below to type again!<span><br><br>" + '<button class = "btn btn-success" onclick = "unpause()" >Type Again</button><br><br>'; // Need to add set of possible responses here...
            
            //add the fbsharestring to message
            fbShareString = '<p>Click the link below to share your score to facebook and challenge your friends!<p><div id="shareBtn" class="btn btn-info clearfix">Share to Facebook</div>';
            message.innerHTML = message.innerHTML + fbShareString;
    
            //listen for sharebutton 
            document.getElementById("shareBtn").addEventListener('click', share);
    
            currentWords.innerHTML = "<span style = 'border-radius: 3px; padding: 1px 3px 1px 3px' id = 'highlightColor'> <strong>That was a good run! </strong>";
            style();
            count = 0; 
    
            // data for google chart and user data storage
            
            scores.push([plays,(wpm/totalTime)*60,(errorCount/totalTime)*60]); // session storage
            lSStore(); // store user data
            drawChart(scores); //chart
    
            // Other data to display
            average = averageScore(scores);
            averageErrors = averageErrorCalc(scores); 
            statistics.innerHTML = "<br><strong>Average WPM: " + average + '<br>' + " Average Errors: " + averageErrors + "<br>" + "Error List: </strong>";
            
            // Add errors from errorlog to display 
            let counter = 0; 
            while(counter < errorLog.length)
            {
                statistics.innerHTML = statistics.innerHTML +  "<br>" + (counter + 1) + ". " + errorLog[counter]; 
                counter ++; 
            }
            errorLog = []; 
            chooseText();
            // Update Game State
            plays += 1;
            time = 0; 
            wpm = 0; 
            errorCount = 0;
            
            
        }
        // Show time 
        timer.innerHTML = time; 
    }
    
    // Checks if the word typed is equal to the word on the text everytime space is pressed. 
    function spacePressed()
    {
         if(!isPaused){
            isPlaying = true;
         }
         
         if(input.value.includes(" "))
         {
            if(matchWords())
            {   
                if(time < totalTime)
                {    
                    wpm++;
                    wpmDisplay.innerHTML = Math.round((60*wpm)/time); 
                }
            } 
            else 
            {
                errorCount++;
            }
    
            
            if(!isPaused){
                count = count + 1;
                errors.innerHTML = Math.round((60*errorCount)/time);
                input.value = '';
                input.placeholder = '';
            }else{
                isPaused = false; 
            }
            showWords(words); 
            
         }
    }
    
    
    // Charts 
    function drawChart(dataTable) 
    {
    var data = google.visualization.arrayToDataTable(dataTable);
    
    var options = {
      //title: 'Your Performance',
      curveType: 'function',
      legend: { position: 'bottom' }
    };
    
    var chart = new google.visualization.LineChart(document.getElementById('curve_chart'));
    
    chart.draw(data, options);
    }
    
    //average wpm calculator  
    
    function averageScore(scores)
    {
    average = 0; 
    let i = 1; 
    while(i < scores.length)
    {
        average = average + scores[i][1]; 
        i++; 
    }
    average = average / (scores.length - 1);
    localStorage.setItem('average', average);
    return average; 
    
    }
    
    //average errors calculator  
    
    function averageErrorCalc(scores)
    {
    averageError = 0; 
    let i = 1; 
    while(i < scores.length)
    {
        averageError = averageError + scores[i][2]; 
        i++;  
    }
    averageError = averageError / (scores.length - 1);
    localStorage.setItem('averageError', averageError);
    return averageError; 
    }
    
    
